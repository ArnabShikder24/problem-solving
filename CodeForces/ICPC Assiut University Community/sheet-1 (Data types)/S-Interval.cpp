#include <bits/stdc++.h>
using namespace std;
#define BOOST ios_base::sync_with_stdio(false); cin.tie(NULL); cout.tie(NULL)

int main() {
    BOOST;
    double n;
    cin >> n;
    if(n >= 0 && n <= 25)
        cout << "Interval [0,25]";
    else if(n > 25 && n <= 50)
        cout << "Interval (25,50]";
    else if(n > 50 && n <= 75)
        cout << "Interval (50,75]";
    else if(n > 75 && n <= 100)
        cout << "Interval (75,100]";
    else 
        cout << "Out of Intervals";
    return 0;
}